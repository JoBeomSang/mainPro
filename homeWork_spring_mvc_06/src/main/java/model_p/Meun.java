package model_p;
 
public class Meun {
	
	String key, value;
	
	

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public Meun(String key, String value) {
		super();
		this.key = key;
		this.value = value;
	}
	
	
	
	

}
